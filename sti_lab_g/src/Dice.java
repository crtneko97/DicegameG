public class Dice {
    int diceNumber = 0;
    int minNumber = 0;
    int maxNumber = 0;
    int rounds = 0;

    public Dice(int diceNumber, int minNumber, int maxNumber, int rounds){
        this.diceNumber = diceNumber;
        this.minNumber = minNumber;
        this.maxNumber = maxNumber;
        this.rounds = rounds;
    }
}
